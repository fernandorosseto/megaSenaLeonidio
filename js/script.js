// Crie um objeto para rastrear a frequência de cada número
const frequenciaNumeros = {};

// Inicialize o objeto frequenciaNumeros com zeros para todos os números possíveis
for (let i = 1; i <= 60; i++) {
	frequenciaNumeros[i] = 0;
}

// Função para atualizar a frequência de um número
function atualizarFrequencia(numero) {
	if (frequenciaNumeros[numero]) {
		frequenciaNumeros[numero]++;
	} else {
		frequenciaNumeros[numero] = 1;
	}
}

// Calcular a frequência de cada número nos jogos
for (const jogo of algunsJogos) {
	for (const numero of jogo.numeros) {
		atualizarFrequencia(numero);
	}
}

console.log(frequenciaNumeros);

// Ordenar os números com base nas frequências em ordem decrescente
const numerosOrdenados = Object.keys(frequenciaNumeros).sort((a, b) => {
	return frequenciaNumeros[b] - frequenciaNumeros[a];
});

// Inicializar grupos A a J
const grupos = {
	A: [],
	B: [],
	C: [],
	D: [],
	E: [],
	F: [],
	G: [],
	H: [],
	I: [],
	J: [],
};

// Distribuir os números nos grupos com base nas frequências
let grupoAtual = "A"; // Comece com o grupo A
for (const numero of numerosOrdenados) {
	const frequencia = frequenciaNumeros[numero];

	// Verificar se o número já está em um grupo anterior
	let numeroAtribuido = false;
	for (let i = "A".charCodeAt(0); i <= grupoAtual.charCodeAt(0); i++) {
		const grupoAnterior = String.fromCharCode(i);
		if (
			grupos[grupoAnterior].length < 7 &&
			frequencia === frequenciaNumeros[grupos[grupoAnterior][0]]
		) {
			grupos[grupoAnterior].push(Number(numero));
			numeroAtribuido = true;
			break;
		}
	}

	// Se o número não foi atribuído a um grupo anterior, coloque-o no grupo atual
	if (!numeroAtribuido) {
		grupos[grupoAtual].push(Number(numero));
	}

	// Avance para o próximo grupo, se necessário
	if (grupos[grupoAtual].length === 7) {
		grupoAtual = String.fromCharCode(grupoAtual.charCodeAt(0) + 1);
	}
}

